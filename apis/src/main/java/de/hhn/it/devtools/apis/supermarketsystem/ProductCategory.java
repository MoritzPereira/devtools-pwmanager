package de.hhn.it.devtools.apis.supermarketsystem;

/**
 * Enum to represent the different types a product can be.
 */
public enum ProductCategory {
  /** Electronic devices. */
  ELECTRONICS,
  /** Clothing like Shoes or Pants. */
  CLOTHES,
  /** Every kind of food. */
  FOOD,
  /** All non-alcoholic drinks. */
  DRINK,
  /** Hygiene products like shampoo or toothbrushes. */
  HYGIENE,
  /** Accessories like Pans, Toys, whatever.  */
  ACCESSORIES,
  /** All alcoholic drinks.  */
  ALCOHOLIC
}
