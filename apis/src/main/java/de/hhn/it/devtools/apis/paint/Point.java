package de.hhn.it.devtools.apis.paint;

public class Point {

    private int xValue;
    private int yValue;


    public Point(int xValue, int yValue) {

        this.xValue = xValue;
        this.yValue = yValue;
    }


    public int getxValue() {
        return xValue;
    }

    public int getyValue() {
        return yValue;
    }

}
